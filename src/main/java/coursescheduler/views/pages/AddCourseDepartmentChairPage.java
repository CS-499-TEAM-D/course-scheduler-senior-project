    public void checkSeats()
    {
        String Seats_string = seatsInput.getText();
        int Seats;
        if (!Seats_string.equals(""))
        {
            try
            {
                Seats = Integer.parseInt(Seats_string.trim());
                addCourseError_Seats.setText("");
                if(Seats > -1)
                {
                    validID = true;
                }
                else
                {
                    addCourseError_Seats.setText("*");
                    validID = false;
                }
            }
            catch (NumberFormatException nfe)
            {
                addCourseError_Seats.setText("*");
                validID = false;
            }
        }
        else
        {
            addCourseError_Seats.setText("*");
            validID = false;
        }
    }
    
